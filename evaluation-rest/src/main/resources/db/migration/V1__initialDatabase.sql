CREATE TABLE EMPLOYEES(
  EMPLOYEE_ID INT(15) UNSIGNED PRIMARY KEY,
  FIRST_NAME VARCHAR(20) NOT NULL,
  LAST_NAME VARCHAR(20) NOT NULL,
  JOB_DESCRIPTION VARCHAR(30) NOT NULL,
  EVALUATOR_ID INT(15)
);

CREATE TABLE MEETINGS(
  MEETING_ID INT(10) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  EMPLOYEE_ID INT(15) UNSIGNED,
  MEETING_DATE DATE,
  SCORE INT(1) UNSIGNED,
  STATUS VARCHAR(15),
  FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (EMPLOYEE_ID)
);

CREATE TABLE CREDENTIALS(
  CREDENTIAL_ID INT(10) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  EMPLOYEE_ID INT(15) UNSIGNED,
  EMAIL VARCHAR(50) NOT NULL,
  PASSWORD VARCHAR(50) NOT NULL,
  FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (EMPLOYEE_ID)

);

